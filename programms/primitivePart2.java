/**
 * float double
*/
public class primitivePart2 {
  public static void main(String[] args) {
    // float -> max and min value
    float myMinFloat = Float.MIN_VALUE;
    float myMaxFloat = Float.MAX_VALUE;
    System.out.println(myMinFloat);
    System.out.println(myMaxFloat);
    // double -> max and min value
    double myMinDouble = Double.MIN_VALUE;
    double myMaxDouble = Double.MAX_VALUE;
    System.out.println(myMinDouble);
    System.out.println(myMaxDouble);

    int myInt = 10;
    float myFloat = 2.582f;
    double myDouble = 2.5896d;
  }
}
